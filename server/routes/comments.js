//======================================================================================
// Import modules that we need =========================================================
//======================================================================================
let express = require ('express');
let router = express.Router();
//======================================================================================
// Get the gravatar icon from email ====================================================
//======================================================================================
let gravatar = require('gravatar');
//======================================================================================
// Get comments model ==================================================================
//======================================================================================
let Comments = require('../models/comments');


//======================================================================================
// Get all comments ====================================================================
//======================================================================================
router.get('/comments', hasAuthorization, (req, res) => {
//list all comments and sort by Date ===================================================
Comments.find().sort('-created').populate('user',
'local.email').exec((error, comments) => {
    if (error) {
        return res.send(400,{
           message : error
        });
    }
//Render result ========================================================================
res.render('comments',{
   title : 'Comments Page',
   comments: comments,
   gravatar: gravatar.url(comments.email, {s:'80', r:'x', d:'retro'},
   true)
   });
  })
});


//======================================================================================
// Create Comments  ====================================================================
//======================================================================================
exports.create = function (req, res) {
// create a new instance of the Comments model with request body =======================
    let comments = new Comments(req.body);
// set current user (id) ===============================================================
    comments.user = req.user;
// save the data received ==============================================================
    comments.save(function (error) {
       if(error) {
           return res.send(400, {
              message:error
           });
       }
// redirect to comments ================================================================
        res.redirect('/comments');
    });
};


//======================================================================================
// Comments authorization middleware  ==================================================
//======================================================================================
exports.hasAuthorization=function (req, res, next) {
    if(req.isAuthenticated())
        return next();
    res.redirect('/login')
};

//======================================================================================
// Exports all the routes to router variable ===========================================
//======================================================================================
module.exports = router;